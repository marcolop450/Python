### Operadores ###

# Basico
print(5 + 3) # Suma
print(5 - 3) # Resta
print(5 * 3) # Multiplicación
print(5 / 3) # División

# Intermedio
print(5 % 3) # Módulo
print(5 ** 3) # Potencia
print(5 // 3) # División entera

# Se puede usar operadores juntos
print((5 + 3) * 2) # Se puede usar operadores juntos

# Concatenacion de cadenas
print("Hola " + " Python") # Concatenación solo con el signo +
print("Hola " + str(5)) # Concatenación con el signo + y forzando el tipo de dato
print("Hola " * 5) # Concatenación con el signo * y repitiendo la cadena
print("Hola " * (5 + 3)) # Concatenación con el signo * y repitiendo la cadena con una operación

### Operadores Comparativos ###

# Comparativos Numeros
print(5 > 3) # Mayor que
print(5 < 3) # Menor que
print(5 >= 3) # Mayor o igual que
print(5 <= 3) # Menor o igual que
print(5 == 3) # Igual que
print(5 != 3) # Diferente que

# Comparativos Cadenas
print("Hola" > "Python") # Mayor que
print("Hola" < "Python") # Menor que
print("Hola" >= "Python") # Mayor o igual que
print("Hola" <= "Python") # Menor o igual que
print("Hola" == "Python") # Igual que
print("Hola" != "Python") # Diferente que

### Operadores Logicos ###

# Comparativos Numeros
print(5 > 3 and 5 < 3) # AND
print(5 > 3 or 5 < 3) # OR
print(not 5 > 3) # NOT}

# Comparativos Cadenas
print("Hola" > "Python" and "Hola" < "Python") # AND
print("Hola" > "Python" or "Hola" < "Python") # OR
print(not "Hola" > "Python") # NOT